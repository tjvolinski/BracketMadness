package com.tjstats.bracket.madness.domain2;

public class Bracket {
	
	private String playerName;
	private String champ;
	private String champRegion;
	private String runnerUp;
	private String runnerUpRegion;
	
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public String getChamp() {
		return champ;
	}
	public void setChamp(String champ) {
		this.champ = champ;
	}
	public String getChampRegion() {
		return champRegion;
	}
	public void setChampRegion(String champRegion) {
		this.champRegion = champRegion;
	}
	public String getRunnerUp() {
		return runnerUp;
	}
	public void setRunnerUp(String runnerUp) {
		this.runnerUp = runnerUp;
	}
	public String getRunnerUpRegion() {
		return runnerUpRegion;
	}
	public void setRunnerUpRegion(String runnerUpRegion) {
		this.runnerUpRegion = runnerUpRegion;
	}
}
